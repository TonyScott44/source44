// Big O Notation
// To analyze the performance of an algorithm, we use Big O Notation.

/* Big O Notation...  

   - provides a numeric representation of the performance of code.

   - provides a high level understanding of the time or space complexity of an algorithm.

   - doesn't care about precision, only about general trends (linear?, quadratic?, constant?)

   It's important to have a precise vocabulary to talk about how code performs.

   When your code slows down or crashes, identifying parts of the code that are inefficient
   can help us find pain points in our applications.

   The time or space complexity (as measured by Big O) depends only on the algorithm, 
   not the hardware used to run the algorithm.

*/


